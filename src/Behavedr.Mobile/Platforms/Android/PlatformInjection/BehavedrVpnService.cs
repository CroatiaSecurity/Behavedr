    private static void EmitSignal(string type, double weight, double confidence)
    {
        while (NetworkSignals.Count >= MaxSignals)
            NetworkSignals.TryDequeue(out _);
        NetworkSignals.Enqueue(new Signal(type, weight, confidence));
    }

    private void CreateNotificationChannel()
    {
        if (Build.VERSION.SdkInt < BuildVersionCodes.O) return;

        var channel = new NotificationChannel(
            ChannelId, "Behavedr Network Monitor", NotificationImportance.Low)
        {
            Description = "Network traffic inspection",
        };
        channel.SetShowBadge(false);
        channel.EnableVibration(false);

        var manager = GetSystemService(NotificationService) as NotificationManager;
        manager?.CreateNotificationChannel(channel);
    }

    private Notification BuildNotification()
    {
        return new Notification.Builder(this, ChannelId)
            .SetContentTitle("Behavedr")
            .SetContentText("Network monitoring active")
            .SetSmallIcon(Resource.Mipmap.appicon)
            .SetOngoing(true)
            .SetCategory(Notification.CategoryService)
            .Build()!;
    }

    private Context _context => ApplicationContext!;

    private enum PacketAction { Forward, Drop, Sinkhole }
}

