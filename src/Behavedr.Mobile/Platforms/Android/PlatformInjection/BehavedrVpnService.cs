using Android.App;
using Android.Content;
using Android.Net;
using Android.OS;
using Behavedr.Core.Models;
using Microsoft.Extensions.Logging;
using Microsoft.Extensions.Logging.Abstractions;
using System.Collections.Concurrent;
using System.Net;
using System.Net.Sockets;

using Signal = Behavedr.Core.Models.Signal;

namespace Behavedr.Mobile.PlatformInjection;

/// <summary>
/// VPN-based network inspection service for non-rooted Android devices.
///
/// On non-rooted devices, iptables is not available. The only way to inspect
/// and control ALL network traffic is via Android's VpnService API.
///
/// This creates a local TUN interface that routes all device traffic through
/// our inspection engine. This allows:
/// - DNS query inspection (detect C2 domain lookups)
/// - IP reputation checking (block known malicious IPs)
/// - Protocol analysis (detect non-standard ports/protocols)
/// - Data exfiltration detection (unusual upload volume)
/// - Network isolation of specific apps (without root)
///
/// Architecture:
/// 1. Establish VPN with TUN interface (no actual remote server)
/// 2. Read raw IP packets from the TUN fd
/// 3. Inspect DNS queries against blocklists
/// 4. Forward clean traffic to real network (via protect() socket)
/// 5. Drop/log blocked traffic
///
/// This is a "local VPN" — traffic never leaves the device except via
/// the normal network interface. No external VPN server is involved.
///
/// v0.2.0 audit fix: Enables network response actions on non-rooted devices.
/// </summary>
[Service(
    Name = "com.croatiasecurity.behavedr.BehavedrVpnService",
    Permission = "android.permission.BIND_VPN_SERVICE",
    Exported = false)]
[IntentFilter(new[] { "android.net.VpnService" })]
public class BehavedrVpnService : VpnService
{
    private const string VpnInterfaceAddress = "10.8.0.2";
    private const string VpnDnsServer = "10.8.0.1";
    private const int VpnMtu = 1500;
    private const string ChannelId = "behavedr_vpn";

    private ParcelFileDescriptor? _vpnInterface;
    private CancellationTokenSource? _cts;
    private Thread? _packetThread;
    private static ILogger? _logger;

    // Blocked domains (C2, malware, phishing)
    private static readonly ConcurrentDictionary<string, DateTime> BlockedDomains = new(StringComparer.OrdinalIgnoreCase);

    // Blocked UIDs (isolated apps)
    private static readonly ConcurrentBag<int> IsolatedUids = new();

    // DNS query log for threat detection
    private static readonly ConcurrentQueue<DnsQueryRecord> RecentDnsQueries = new();
    private const int MaxDnsRecords = 500;

    // Signals generated by network inspection
    private static readonly ConcurrentQueue<Signal> NetworkSignals = new();
    private const int MaxSignals = 100;

    public static void SetLogger(ILogger? logger) => _logger = logger;

    /// <summary>
    /// Add a domain to the blocklist. Queries for this domain will be sinkholed.
    /// </summary>
    public static void BlockDomain(string domain)
    {
        BlockedDomains[domain] = DateTime.UtcNow;
        _logger?.LogWarning("[BehavedrVPN] Blocked domain: {Domain}", domain);
    }

    /// <summary>
    /// Isolate a specific app UID from network access via the VPN.
    /// </summary>
    public static void IsolateUid(int uid)
    {
        IsolatedUids.Add(uid);
        _logger?.LogWarning("[BehavedrVPN] Isolated UID: {Uid}", uid);
    }

    /// <summary>
    /// Get recent DNS queries for analysis.
    /// </summary>
    public static IReadOnlyList<DnsQueryRecord> GetRecentDnsQueries()
    {
        return RecentDnsQueries.ToArray();
    }

    /// <summary>
    /// Drain accumulated network signals.
    /// </summary>
    public static IReadOnlyList<Signal> DrainSignals()
    {
        var signals = new List<Signal>();
        while (NetworkSignals.TryDequeue(out var signal))
        {
            signals.Add(signal);
        }
        return signals;
    }

    /// <summary>
    /// Prepare the VPN consent intent. The user must approve VPN connection.
    /// Returns null if already approved (consent cached by system).
    /// </summary>
    public static Intent? PrepareVpnConsent(Context context)
    {
        return VpnService.Prepare(context);
    }

    public override void OnCreate()
    {
        base.OnCreate();
        CreateNotificationChannel();
    }

    public override StartCommandResult OnStartCommand(Intent? intent, StartCommandFlags flags, int startId)
    {
        if (intent?.Action == "STOP")
        {
            StopVpn();
            StopSelf();
            return StartCommandResult.NotSticky;
        }

        StartVpn();
        return StartCommandResult.Sticky;
    }

    public override void OnDestroy()
    {
        StopVpn();
        base.OnDestroy();
    }

    public override void OnRevoke()
    {
        // User revoked VPN permission
        StopVpn();
        EmitSignal("vpn_permission_revoked", 50, 0.7);
        base.OnRevoke();
    }

    private void StartVpn()
    {
        try
        {
            var builder = new Builder(this)
                .SetSession("Behavedr Network Monitor")
                .AddAddress(VpnInterfaceAddress, 32)
                .AddDnsServer(VpnDnsServer)
                .AddRoute("0.0.0.0", 0) // Route all IPv4 traffic
                .SetMtu(VpnMtu)
                .SetBlocking(true);

            // On API 29+, set metered to avoid affecting user data warnings
            if (Build.VERSION.SdkInt >= BuildVersionCodes.Q)
            {
#pragma warning disable CA1416
                builder.SetMetered(false);
#pragma warning restore CA1416
            }

            // Exclude ourselves from VPN to prevent routing loops
            builder.AddDisallowedApplication(_context.PackageName!);

            _vpnInterface = builder.Establish();
            if (_vpnInterface is null)
            {
                _logger?.LogWarning("[BehavedrVPN] Failed to establish VPN interface");
                return;
            }

            // Show persistent notification
            var notification = BuildNotification();
            StartForeground(2, notification);

            // Start packet processing thread
            _cts = new CancellationTokenSource();
            _packetThread = new Thread(() => PacketLoop(_cts.Token))
            {
                Name = "BehavedrVPN-Packet",
                IsBackground = true,
                Priority = System.Threading.ThreadPriority.AboveNormal,
            };
            _packetThread.Start();

            _logger?.LogInformation("[BehavedrVPN] VPN established — monitoring all device traffic");
        }
        catch (Exception ex)
        {
            _logger?.LogError(ex, "[BehavedrVPN] Failed to start VPN");
        }
    }

    private void StopVpn()
    {
        _cts?.Cancel();
        _packetThread?.Join(2000);
        _vpnInterface?.Close();
        _vpnInterface = null;
        _cts?.Dispose();
        _cts = null;
    }

    /// <summary>
    /// Main packet processing loop. Reads IP packets from TUN, inspects them,
    /// and forwards clean traffic to the real network.
    /// </summary>
    private void PacketLoop(CancellationToken ct)
    {
        var buffer = new byte[VpnMtu + 4]; // IP packet + possible alignment
        var fd = _vpnInterface!.Fd;

        using var inputStream = new Java.IO.FileInputStream(_vpnInterface.FileDescriptor);
        using var outputStream = new Java.IO.FileOutputStream(_vpnInterface.FileDescriptor);

        while (!ct.IsCancellationRequested)
        {
            try
            {
                var length = inputStream.Read(buffer, 0, buffer.Length);
                if (length <= 0)
                {
                    Thread.Sleep(10);
                    continue;
                }

                var packet = new ReadOnlySpan<byte>(buffer, 0, length);
                var action = InspectPacket(packet);

                switch (action)
                {
                    case PacketAction.Forward:
                        ForwardPacket(buffer, length, outputStream);
                        break;

                    case PacketAction.Drop:
                        // Silently dropped — logged in InspectPacket
                        break;

                    case PacketAction.Sinkhole:
                        // Return NXDOMAIN or RST
                        SendSinkholeResponse(buffer, length, outputStream);
                        break;
                }
            }
            catch (Java.IO.IOException)
            {
                if (!ct.IsCancellationRequested)
                    break; // VPN interface closed
            }
            catch (Exception ex)
            {
                _logger?.LogDebug(ex, "[BehavedrVPN] Packet processing error");
            }
        }
    }

    /// <summary>
    /// Inspect a raw IP packet and determine action.
    /// </summary>
    private PacketAction InspectPacket(ReadOnlySpan<byte> packet)
    {
        if (packet.Length < 20) return PacketAction.Forward; // Too small for IP header

        // Parse IP header version
        var version = (packet[0] >> 4) & 0x0F;
        if (version != 4) return PacketAction.Forward; // Only inspect IPv4 for now

        var headerLength = (packet[0] & 0x0F) * 4;
        var protocol = packet[9]; // Protocol field
        var destIp = new IPAddress(packet.Slice(16, 4));

        // Check if destination is in blocked list
        if (IsIpBlocked(destIp))
        {
            EmitSignal($"blocked_connection:{destIp}", 70, 0.82);
            return PacketAction.Drop;
        }

        // DNS inspection (UDP port 53)
        if (protocol == 17 && packet.Length > headerLength + 8) // UDP
        {
            var udpDestPort = (packet[headerLength + 2] << 8) | packet[headerLength + 3];
            if (udpDestPort == 53)
            {
                return InspectDnsQuery(packet.Slice(headerLength + 8));
            }
        }

        // TCP connection inspection
        if (protocol == 6 && packet.Length > headerLength + 20) // TCP
        {
            var tcpDestPort = (packet[headerLength + 2] << 8) | packet[headerLength + 3];
            var tcpFlags = packet[headerLength + 13];

            // SYN packet (new connection)
            if ((tcpFlags & 0x02) != 0)
            {
                InspectNewConnection(destIp, tcpDestPort);
            }
        }

        return PacketAction.Forward;
    }

    /// <summary>
    /// Inspect DNS queries for C2 domains and DGA patterns.
    /// </summary>
    private PacketAction InspectDnsQuery(ReadOnlySpan<byte> dnsPayload)
    {
        if (dnsPayload.Length < 12) return PacketAction.Forward;

        try
        {
            // Parse DNS query name
            var domain = ParseDnsName(dnsPayload, 12);
            if (string.IsNullOrEmpty(domain)) return PacketAction.Forward;

            // Record the query
            var record = new DnsQueryRecord(domain, DateTime.UtcNow);
            RecentDnsQueries.Enqueue(record);
            while (RecentDnsQueries.Count > MaxDnsRecords)
                RecentDnsQueries.TryDequeue(out _);

            // Check blocklist
            if (BlockedDomains.ContainsKey(domain) ||
                BlockedDomains.Keys.Any(b => domain.EndsWith("." + b, StringComparison.OrdinalIgnoreCase)))
            {
                EmitSignal($"dns_blocked:{domain}", 75, 0.88);
                return PacketAction.Sinkhole;
            }

            // DGA detection: high-entropy domain names
            if (IsPotentialDga(domain))
            {
                EmitSignal($"dga_domain_detected:{domain}", 70, 0.78);
                // Don't block — just flag (could be CDN or legitimate service)
            }

            // Detect DNS tunneling (long subdomain labels)
            if (IsDnsTunneling(domain))
            {
                EmitSignal($"dns_tunneling_detected:{domain}", 80, 0.85);
            }
        }
        catch { }

        return PacketAction.Forward;
    }

    /// <summary>
    /// Inspect new TCP connections for suspicious patterns.
    /// </summary>
    private void InspectNewConnection(IPAddress destIp, int destPort)
    {
        // Non-standard ports for web traffic (potential C2)
        if (destPort is not (80 or 443 or 8080 or 8443) and > 1024)
        {
            // High ports could be legitimate (app services) or C2
            // Only flag if combined with other indicators
        }

        // Tor exit node connections (port 9001, 9030)
        if (destPort is 9001 or 9030 or 9050 or 9150)
        {
            EmitSignal($"tor_connection:{destIp}:{destPort}", 65, 0.75);
        }

        // Known C2 ports
        if (destPort is 4444 or 5555 or 1337 or 31337 or 6667 or 6697)
        {
            EmitSignal($"suspicious_port:{destIp}:{destPort}", 60, 0.7);
        }
    }

    /// <summary>
    /// Detect potential DGA (Domain Generation Algorithm) domains.
    /// High character entropy + length suggest algorithmically generated names.
    /// </summary>
    private static bool IsPotentialDga(string domain)
    {
        // Extract the registrable domain (second-level)
        var parts = domain.Split('.');
        if (parts.Length < 2) return false;

        var sld = parts[^2]; // Second-level domain
        if (sld.Length < 8) return false;

        // Calculate Shannon entropy
        var freq = new Dictionary<char, int>();
        foreach (var c in sld)
        {
            freq[c] = freq.GetValueOrDefault(c) + 1;
        }

        double entropy = 0;
        foreach (var count in freq.Values)
        {
            var p = (double)count / sld.Length;
            entropy -= p * Math.Log2(p);
        }

        // High entropy (>3.5) + length (>12) is suspicious
        return entropy > 3.5 && sld.Length > 12;
    }

    /// <summary>
    /// Detect DNS tunneling by checking for unusually long subdomain labels.
    /// DNS tunneling encodes data in subdomain labels (>50 chars).
    /// </summary>
    private static bool IsDnsTunneling(string domain)
    {
        var parts = domain.Split('.');
        return parts.Any(p => p.Length > 50) ||
               parts.Sum(p => p.Length) > 150;
    }

    private static bool IsIpBlocked(IPAddress ip)
    {
        // Check against known C2 IP ranges (simplified — in production, use threat intel feed)
        // Private IPs are always allowed
        var bytes = ip.GetAddressBytes();
        if (bytes[0] == 10 || (bytes[0] == 172 && bytes[1] >= 16 && bytes[1] <= 31) ||
            (bytes[0] == 192 && bytes[1] == 168))
            return false;

        return false; // Rely on domain blocklist primarily
    }

    private static string? ParseDnsName(ReadOnlySpan<byte> data, int offset)
    {
        var parts = new List<string>();
        int pos = offset;
        int maxIterations = 50;

        while (pos < data.Length && maxIterations-- > 0)
        {
            var labelLen = data[pos];
            if (labelLen == 0) break;

            // Pointer
            if ((labelLen & 0xC0) == 0xC0)
            {
                var ptrOffset = ((labelLen & 0x3F) << 8) | data[pos + 1];
                return ParseDnsName(data, ptrOffset) is { } remainder
                    ? string.Join(".", parts.Append(remainder))
                    : string.Join(".", parts);
            }

            pos++;
            if (pos + labelLen > data.Length) break;
            parts.Add(System.Text.Encoding.ASCII.GetString(data.Slice(pos, labelLen)));
            pos += labelLen;
        }

        return parts.Count > 0 ? string.Join(".", parts) : null;
    }

